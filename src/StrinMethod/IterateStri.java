package StrinMethod;

public class IterateStri {

	public static void main(String[] args) {
		
		String s= "Greens Technology";
		
		// To iterate the string & print no of e
		
		for (int i = 0; i <17; i++) {
			
			char ch=s.charAt(i);
			if (ch=='e') {

				System.out.println(ch);

			}
			
			
			
		}
		
			
		
	}
}
